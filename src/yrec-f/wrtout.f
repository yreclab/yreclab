       WRITE(ISHORT,30)MODEL,SMASS,XNEW,ZNEW,DAGE,DDAGE
   30    FORMAT(1X,'MODEL NO.',I5,2X,'MASS',F13.7,2X,'(X,Z)=(',F11.9,
