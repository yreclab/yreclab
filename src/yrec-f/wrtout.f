   70 FORMAT(1X,'CENTER: LOG P=',F10.7,' LOG T=',F10.8,' LOG D=',
     *F10.6,' BETA=',F9.7,' ETA=',0PF10.5,'  X=',0PF9.7,' Z=',F9.7,
     *' O16=',F9.7)
      WRITE(ISHORT,80)(TLUMX(J),J = 1,5),TLUMX(8),TLUMX(6),TLUMX(7)
   80 FORMAT(1X,'ENERGY: PPI',1PE13.6,'  PPII',E13.6,'  PPIII',E13.6,
