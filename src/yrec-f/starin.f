          WRITE(ISHORT,1040) CMIXL,CMIXL0,LEXCOM,LEXCP0
          WRITE(IOWR,1040) CMIXL,CMIXL0,LEXCOM,LEXCP0
 1040       FORMAT(1X,'ERROR IN SUBROUTINE STARIN'/1X,'USER PARAMETERS',
